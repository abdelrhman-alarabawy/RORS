    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        additemerrorLabel.setText("");
        String item_name;
        String menu_name=null;
        int price,menu_id=0;
        String description=null;
        item_name=itemnameText.getText();
        menu_name=String.valueOf(menuComboBox.getSelectedItem());
        try{
            price=0;
            price=Integer.parseInt(priceText.getText());
            if(price<=0)
                throw new Exception();
            switch(menu_name)
            {
                case "Main Dish": menu_id=1;break;
                case "Dessert": menu_id=2;break;
                case "Hot Drink": menu_id=3;break;
                case "Cold Drink": menu_id=4;break;
            }
            description=descriptionText.getText();
            manager.addrecord(item_name,menu_name,menu_id,price,description);
            
        }
        catch(Exception ex)
        {
            System.out.println(ex.toString());
            additemerrorLabel.setText("Unaccepted Data..!");
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

