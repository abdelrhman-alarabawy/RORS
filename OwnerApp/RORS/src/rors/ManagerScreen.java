    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jLabel3.setText("");
        String new_price=jTextField1.getText();
        
        try{
            int price =0; 
            price= Integer.valueOf(new_price);
            if(price<=0)
                throw new Exception();
            else{
                manager.update_price(price,String.valueOf(jComboBox1.getSelectedItem()));
                jLabel3.setText("Updated Succesfully!");
            }
        }
        catch(Exception ex)
                {
                    jLabel3.setText("Un Accepted input..!");
                }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

