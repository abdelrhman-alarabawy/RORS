    public void RORS_login() {
        int x = 0;
        try {

            Connection conn = DB_connection.db_connection();
            String sql = "SELECT * FROM customer where  phone=? and password=?";

            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, phone_num.getText());
            pst.setString(2, password.getText());
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {

                RORS_CUSTOMER_Restaurant r = new RORS_CUSTOMER_Restaurant(true,phone_num.getText().toString());
                r.setVisible(true);
                this.setVisible(false);
            } else {

                JOptionPane.showMessageDialog(null, "invalid input");
                phone_num.setText("");
                password.setText("");
            }

        } catch (Exception e) {
            phone_num.setText("");
            password.setText(""); 

            JOptionPane.showMessageDialog(null, "error occured ");

        }

    }

